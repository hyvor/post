<h2 id="history">History</h2>

<ul>
	<li>
		<strong> August, 2024 </strong>: Launched a minimal newsletter feature in Hyvor Talk. This
		was helpful for our team to understand the newsletter industry and its requirements. We
		learned that privacy and simplicity are problems worth solving.
	</li>
	<li>
		<strong> January, 2025 </strong>: Started developing Hyvor Post as a standalone product.
	</li>
	<li>
		<strong> May, 2025 </strong>: Realized that relying on a third-party email service provider
		(ESP) would compromise our commitment to privacy. Decided to build our own email delivery
		system to ensure complete control over user data and privacy. This was a significant
		undertaking, but it was essential to stay true to our values.
		<a href="https://relay.hyvor.com">Hyvor Relay</a>, an open-source email delivery system, was
		born.
	</li>
	<li>
		<strong> November, 2025 </strong>: Launched Hyvor Post.
	</li>
</ul>
