<h2 id="why-privacy">Why Privacy?</h2>

<p>
	At HYVOR, we believe that privacy is a fundamental right. In today's digital age, where data is
	often exploited for profit, we wanted to create a platform that respects user privacy and
	provides a simple, effective way to manage newsletters without intrusive tracking.
</p>

<p>
	There is an increasing number of creators and businesses who want to communicate with their
	audience without compromising their privacy. Hyvor Post is our answer to that need.
</p>

<h3 id="no-tracking">No Tracking</h3>

<p>
	Open & click tracking is standard in the industry. However, most users are not aware of the
	extent of tracking involved. This type of tracking can reveal sensitive information about users,
	such as their interests, behaviors, and even location. In Hyvor Post, we have completely
	eliminated open & click tracking. There is no hidden switch to turn on tracking. If it's a
	newsletter running on Hyvor Post, users can be assured that there is no tracking.
</p>

